package com.ctseducare.kotlin.k01_variables_datatypes

fun main() {

    var decimal = 123
    var long = 123L
    var hexa = 0x7B  // 123 em hexadecimal
    var binary = 0b1111011  // 123 em binário
    // Literais octais não são suportados pelo Kotlin

    println(decimal)
    println(long)
    println(hexa)
    println(binary)

}
