package com.ctseducare.kotlin.k02_operators

fun main() {

    var n1 = 20
    var n2 = 10

    var adicao = n1 + n2
    println(adicao)

    var subtracao = n1 - n2
    println(subtracao)

    var multiplicacao = n1 * n2
    println(multiplicacao)

    var divisao = n1 / n2
    println(divisao)

    var resto = n1 % n2
    println(resto)

}